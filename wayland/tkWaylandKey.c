/*
 * ----------------------------------------------------------------------------
 * FreeIbusContext --
 *
 *         Free all resources owned by an IbusContext.  Does not unlink it
 *         from the global list.
 *
 * Results:
 *         None.
 *
 * Side effects:
 *         Unregisters signal slots and frees memory.
 * ----------------------------------------------------------------------------
 */

static void
FreeIbusContext(IbusContext *ctx)
{
    if (!ctx) return;

    if (ctx->preeditText) {
        free(ctx->preeditText);
        ctx->preeditText = NULL;
    }
    if (ctx->show_slot) {
        sd_bus_slot_unref(ctx->show_slot);
        ctx->show_slot = NULL;
    }
    if (ctx->hide_slot) {
        sd_bus_slot_unref(ctx->hide_slot);
        ctx->hide_slot = NULL;
    }
    if (ctx->preedit_slot) {
        sd_bus_slot_unref(ctx->preedit_slot);
        ctx->preedit_slot = NULL;
    }
    if (ctx->signal_slot) {
        sd_bus_slot_unref(ctx->signal_slot);
        ctx->signal_slot = NULL;
    }
    if (ctx->obj_path) {
        free(ctx->obj_path);
        ctx->obj_path = NULL;
    }
    Tcl_Free(ctx);
}

/*
 * ----------------------------------------------------------------------------
 * RemoveIbusContext --
 *
 *         Remove a context from the global list and free it.
 *         Called on window destruction.
 *
 * Results:
 *         None.
 *
 * Side effects:
 *         None.
 * ----------------------------------------------------------------------------
 */

void
RemoveIbusContext(Tk_Window tkwin)
{
    IbusContext *ctx = all_contexts;
    IbusContext **prev = &all_contexts;

    while (ctx) {
        if (ctx->tkwin == tkwin) {
            *prev = ctx->next;

            /*
             * Mark the context destroyed before draining.  Any signals that
             * fire during the drain will see the flag and return immediately
             * without touching tkwin or focusedWidget.
             */
            ctx->destroyed = 1;
            ctx->focusedWidget = NULL;

            /*
             * Drain any signals already queued on the D-Bus file descriptor
             * so that their handlers run while ctx is still allocated.  This
             * prevents a use-after-free when a CommitText or UpdatePreedit
             * signal was buffered just before the window closed.
             */
            if (ibus_bus) {
                int n;
                do { n = sd_bus_process(ibus_bus, NULL); } while (n > 0);
            }

            FreeIbusContext(ctx);
            return;
        }
        prev = &ctx->next;
        ctx = ctx->next;
    }
}

