/*
 * ----------------------------------------------------------------------------
 * InitializeXKB --
 *
 *         Initialize the XKB context, keymap, compose table, and state.
 *
 * Results:
 *         Returns 1 on success, 0 on failure.
 *
 * Side effects:
 *         Allocates global XKB resources.
 * ----------------------------------------------------------------------------
 */

static bool
InitializeXKB(void)
{
    const char *locale;

    /* Create an XKB context. */
    xkbState.context = xkb_context_new(XKB_CONTEXT_NO_FLAGS);
    if (!xkbState.context) {
        return false;
    }

    /* Obtain the locale for compose sequences. */
    locale = getenv("LC_ALL");
    if (!locale) locale = getenv("LC_CTYPE");
    if (!locale) locale = getenv("LANG");
    if (!locale) locale = "C";

    /* Create a compose table. */
    xkbState.compose_table = xkb_compose_table_new_from_locale(
            xkbState.context, locale, XKB_COMPOSE_COMPILE_NO_FLAGS);

    if (xkbState.compose_table) {
        xkbState.compose_state = xkb_compose_state_new(
                xkbState.compose_table, XKB_COMPOSE_STATE_NO_FLAGS);
    }

    /* Compile a keymap (default US layout as a starting point). */
    struct xkb_rule_names names = {
        .rules = "evdev",
        .model = "pc105",
        .layout = "us",
        .variant = "",
        .options = ""
    };

    xkbState.keymap = xkb_keymap_new_from_names(xkbState.context,
                          &names, XKB_KEYMAP_COMPILE_NO_FLAGS);
    if (!xkbState.keymap) {
        return false;
    }

    /* Create the XKB state object. */
    xkbState.state = xkb_state_new(xkbState.keymap);
    if (!xkbState.state) {
        return false;
    }

    return true;
}

/*
 * ----------------------------------------------------------------------------
 * CleanupXKB --
 *
 *         Free all XKB resources.
 *
 * Results:
 *         None.
 *
 * Side effects:
 *         The global XKB state is reset to NULL.
 * ----------------------------------------------------------------------------
 */

static void
CleanupXKB(void)
{
    if (xkbState.compose_state) {
        xkb_compose_state_unref(xkbState.compose_state);
        xkbState.compose_state = NULL;
    }
    if (xkbState.compose_table) {
        xkb_compose_table_unref(xkbState.compose_table);
        xkbState.compose_table = NULL;
    }
    if (xkbState.state) {
        xkb_state_unref(xkbState.state);
        xkbState.state = NULL;
    }
    if (xkbState.keymap) {
        xkb_keymap_unref(xkbState.keymap);
        xkbState.keymap = NULL;
    }
    if (xkbState.context) {
        xkb_context_unref(xkbState.context);
        xkbState.context = NULL;
    }
}

/*
 * ----------------------------------------------------------------------------
 * TkWaylandKeyInit --
 *
 *         Initialize the Wayland keyboard subsystem, including XKB and IME
 *         (IBus) integration.
 *
 *         IBus initialization is treated as optional: if the IBus daemon is
 *         not running (common for non-CJK users), the keyboard still functions
 *         correctly via XKB alone.
 *
 * Results:
 *         Returns true on success, false on failure (XKB failure only).
 *
 * Side effects:
 *         Creates the XKB context and attempts to start the IBus D-Bus
 *         connection.
 * ----------------------------------------------------------------------------
 */

bool
TkWaylandKeyInit(void)
{
    if (!InitializeXKB()) {
        fprintf(stderr, "TkWaylandKeyInit: failed to initialize xkb.\n");
        return false;
    }
    TkMainInfo *info = TkGetMainInfoList();
    /* IBus failure is non-fatal; keyboard works without it. */
    TkWaylandIbus_Init(info->interp);
    return true;
}

/*
 * ----------------------------------------------------------------------------
 * TkWaylandKeyCleanup --
 *
 *         Clean up all Wayland keyboard resources, including XKB.
 *
 * Results:
 *         None.
 *
 * Side effects:
 *         Frees the XKB state, keymap, and context.
 * ----------------------------------------------------------------------------
 */

void
TkWaylandKeyCleanup() {
    CleanupXKB();
}

/*
 * Input Method Editor implementation.
 *
 * This implementation is necessary because GLFW does not provide any built-in
 * support for IME despite requests dating back many years.  We use sd-bus to
 * connect to the IBus IME daemon.  IBus was chosen because it is widely used,
 * and the other primary IME library, Fcitx, provides a compatibility interface
 * for IBus.  IBus provides the UI (candidate windows, preëdit) and creates a
 * context for each Tk toplevel, then forwards committed text to the focused
 * text widget via a Tcl command.
 *
 * We use the direct D-Bus connection rather than the Wayland text-input
 * protocols because those would require managing a separate Wayland surface
 * with GLFW, which is complex and fragile.  GLFW does not expose any way to
 * hook directly into its Wayland surface.
 *
 * IBus D-Bus signal signatures (real wire format):
 *
 *   CommitText        (v)   – IBus.Text variant containing the committed string
 *   UpdatePreeditText (vub) – IBus.Text variant, uint32 cursor, bool visible
 *   HidePreeditText   ()    – hide the preedit (no arguments)
 *   ShowPreeditText   ()    – show the preedit (no arguments)
 *
 * Note: the signal is named "UpdatePreeditText" (not "UpdatePreedit").
 * Older documentation and some examples use the shorter name but modern
 * IBus engines (including Mozc and iBus-libpinyin) emit the full name.
 *
 * IBus.Text is a D-Bus struct: (sa{sv}sv) — (name, attachments, text,
 * attributes).  We unpack only the plain text string for simplicity.
 */


 /* IBus D-Bus constants. */

#define IBUS_SERVICE          "org.freedesktop.IBus"
#define IBUS_PATH             "/org/freedesktop/IBus"
#define IBUS_INTERFACE        "org.freedesktop.IBus"
#define IBUS_IC_INTERFACE     "org.freedesktop.IBus.InputContext"

/* IBus signal names. */
#define IBUS_SIGNAL_COMMIT_TEXT      "CommitText"
#define IBUS_SIGNAL_UPDATE_PREEDIT   "UpdatePreeditText"
#define IBUS_SIGNAL_HIDE_PREEDIT     "HidePreeditText"
#define IBUS_SIGNAL_SHOW_PREEDIT     "ShowPreeditText"

/* Bitmask identifiers defined by the IBus wire specification */
#define IBUS_CAP_PREEDIT_TEXT   (1u << 0)
#define IBUS_CAP_AUXILIARY_TEXT (1u << 1)
#define IBUS_CAP_LOOKUP_TABLE   (1u << 2)
#define IBUS_CAP_FOCUS          (1u << 3)


/* Per-window IBus context structure. */

typedef struct IbusContext {
    Tk_Window tkwin;            /* Tk toplevel this context belongs to. */
    Tk_Window focusedWidget;    /* Sub-widget currently holding keyboard focus.
                                 * Set by TkWaylandIbusFocusIn, cleared on
                                 * FocusOut.  Used by signal handlers instead
                                 * of dispPtr->focusPtr, which is unreliable on
                                 * Wayland. */
    char *obj_path;             /* D-Bus object path of the IBus input context. */
    sd_bus_slot *signal_slot;   /* Slot for CommitText signal subscription. */
    sd_bus_slot *preedit_slot;  /* Slot for UpdatePreeditText signal subscription. */
    sd_bus_slot *hide_slot;     /* Slot for HidePreeditText signal subscription. */
    sd_bus_slot *show_slot;     /* Slot for ShowPreeditText signal subscription. */
    Tcl_Interp *interp;         /* Tcl interpreter (for callbacks). */
    int enabled;                /* Whether IME is active for this window. */
    int composing;              /* Non-zero while IBus owns an active composition.
                                 * Set on UpdatePreeditText/ShowPreeditText,
                                 * cleared on CommitText/HidePreeditText.
                                 * Used by the GLFW char callback to suppress
                                 * raw latin input during IME composition. */
    int destroyed;              /* Set to 1 when the Tk window has been
                                 * destroyed; guards signal handlers against
                                 * using a stale tkwin pointer. */
    char *preeditText;          /* Current preedit/composing text (owned) */
    struct IbusContext *next;   /* Linked list pointer. */
} IbusContext;

/* Global IBus state. */
sd_bus *ibus_bus = NULL;
IbusContext *all_contexts = NULL;

/*
 * Re-entrancy guard for sd_bus_process.  Set to 1 while
 * IbusProcessKeyEvent is draining the bus synchronously so that
 * IbusBusHandler (the Tcl file handler) does not call sd_bus_process
 * concurrently on the same bus object.
 */
static int ibus_draining = 0;

/*
 * ----------------------------------------------------------------------------
 * FindContext --
 *
 *         Find the IBus context associated with a given Tk window.
 *
 * Results:
 *         Returns a pointer to the IbusContext structure, or NULL if no
 *         context exists for the window.
 *
 * Side effects:
 *         None.
 * ----------------------------------------------------------------------------
 */

static IbusContext *
FindContext(Tk_Window tkwin)
{
    IbusContext *ctx = all_contexts;
    while (ctx) {
        if (ctx->tkwin == tkwin)
            return ctx;
        ctx = ctx->next;
    }
    return NULL;
}
/*
 *----------------------------------------------------------------------
 *
 * IbusReadTextFromVariant --
 *
 * Helper to safely unpack an IBusText object wrapped inside a
 * D-Bus variant container. Dynamically inspects the signature to
 * handle protocol variations safely across different IBus versions.
 *
 * Results:
 * 0 on success, negative error code on failure.
 *
 * Side effects:
 * Moves message container cursor; sets text_out to point inside
 * the message buffer.
 *
 *----------------------------------------------------------------------
 */

static int
IbusReadTextFromVariant(
    sd_bus_message *m,
    const char **text_out)
{
    int r;
    char type;
    const char *contents = NULL;
    const char *objtype = NULL;

    *text_out = NULL;

    /* Expect a variant. */
    r = sd_bus_message_peek_type(m, &type, &contents);
    fprintf(stderr,
            "peek: r=%d type='%c' contents='%s'\n",
            r,
            type,
            contents ? contents : "(null)");

    if (r < 0) {
        return r;
    }

    if (type != SD_BUS_TYPE_VARIANT) {
        fprintf(stderr,
                "Expected VARIANT, got '%c'\n",
                type);
        return -EINVAL;
    }

    /* Enter variant. */
    r = sd_bus_message_enter_container(
            m,
            SD_BUS_TYPE_VARIANT,
            contents);
    fprintf(stderr,
            "enter variant -> r=%d\n",
            r);

    if (r < 0) {
        return r;
    }

    /* See what's actually inside. */
    r = sd_bus_message_peek_type(m, &type, &contents);
    fprintf(stderr,
            "inside variant: r=%d type='%c' contents='%s'\n",
            r,
            type,
            contents ? contents : "(null)");

    if (r < 0) {
        goto cleanup_variant;
    }

    if (type != SD_BUS_TYPE_STRUCT) {
        fprintf(stderr,
                "Expected STRUCT inside variant, got '%c'\n",
                type);
        r = -EINVAL;
        goto cleanup_variant;
    }

    /* Enter struct. */
    r = sd_bus_message_enter_container(
            m,
            SD_BUS_TYPE_STRUCT,
            NULL);
    fprintf(stderr,
            "enter struct -> r=%d\n",
            r);

    if (r < 0) {
        goto cleanup_variant;
    }

    /* First field should be "IBusText". */
    r = sd_bus_message_read(m, "s", &objtype);
    fprintf(stderr,
            "read object type -> r=%d value='%s'\n",
            r,
            objtype ? objtype : "(null)");

    if (r < 0) {
        goto cleanup_struct;
    }

    /* What comes next? */
    r = sd_bus_message_peek_type(m, &type, &contents);
    fprintf(stderr,
            "after object type: r=%d type='%c' contents='%s'\n",
            r,
            type,
            contents ? contents : "(null)");

    if (r < 0) {
        goto cleanup_struct;
    }

    /*
     * Old IBus encodings typically have an a{sv}
     * attributes dictionary here.
     */
    if (type == SD_BUS_TYPE_ARRAY) {
        fprintf(stderr,
                "skipping array '%s'\n",
                contents ? contents : "(null)");

        r = sd_bus_message_skip(m, "a{sv}");
        fprintf(stderr,
                "skip array -> r=%d\n",
                r);

        if (r < 0) {
            goto cleanup_struct;
        }
    }

    /* See what field follows. */
    r = sd_bus_message_peek_type(m, &type, &contents);
    fprintf(stderr,
            "before text read: r=%d type='%c' contents='%s'\n",
            r,
            type,
            contents ? contents : "(null)");

    if (r < 0) {
        goto cleanup_struct;
    }

    /* Try to read UTF-8 text. */
    r = sd_bus_message_read(m, "s", text_out);
    fprintf(stderr,
            "read text -> r=%d text='%s'\n",
            r,
            (r >= 0 && *text_out) ? *text_out : "(null)");

cleanup_struct:
    sd_bus_message_exit_container(m);

cleanup_variant:
    sd_bus_message_exit_container(m);

    return r;
}

/*
 * ----------------------------------------------------------------------------
 * TkWaylandSendUnicodeString --
 *
 *         Generate synthetic KeyPress events for committed or preedit text.
 *         Builds a complete XEvent.
 *
 * Results:
 *         None.
 *
 * Side effects:
 *         Queues KeyPress events to the correct widget.
 * ----------------------------------------------------------------------------
 */

static void
TkWaylandSendUnicodeString(
    Tk_Window tkwin,
    const char *utf8_str)
{
    if (!utf8_str || !*utf8_str || !tkwin) {
        return;
    }

    /*
     * Stored text lives on the toplevel's privatePtr->pendingText and is
     * read by TkpGetString to populate %A in tkBind.c.  Walk up to find
     * the toplevel so we can set it before queuing the synthetic KeyPress.
     */
    TkWindow *topPtr = (TkWindow *)tkwin;
    while (topPtr && !Tk_IsTopLevel(topPtr)) {
        topPtr = (TkWindow *)Tk_Parent(topPtr);
    }
    if (!topPtr) return;

    Display *display = Tk_Display(tkwin);
    Window window = Tk_WindowId(tkwin);

    Tcl_Time now;
    Tcl_GetTime(&now);
    Time time = (Time)(now.sec * 1000 + now.usec / 1000);

    XEvent xEvent;
    memset(&xEvent, 0, sizeof(XEvent));

    xEvent.xany.display = display;
    xEvent.xany.window  = window;
    xEvent.xany.type    = KeyPress;

    xEvent.xkey.root        = XRootWindow(display, 0);
    xEvent.xkey.time        = time;
    xEvent.xkey.same_screen = True;
    xEvent.xkey.state       = 0;

    /* Add current modifiers from XKB if available. */
    if (xkbState.state && xkbState.keymap) {
        xkb_mod_mask_t mods = xkb_state_serialize_mods(xkbState.state,
                                XKB_STATE_MODS_DEPRESSED | XKB_STATE_MODS_LATCHED);
        if (mods & (1 << xkb_keymap_mod_get_index(xkbState.keymap, "Shift")))
            xEvent.xkey.state |= ShiftMask;
        if (mods & (1 << xkb_keymap_mod_get_index(xkbState.keymap, "Control")))
            xEvent.xkey.state |= ControlMask;
        if (mods & (1 << xkb_keymap_mod_get_index(xkbState.keymap, "Alt")))
            xEvent.xkey.state |= Mod1Mask;
        if (mods & (1 << xkb_keymap_mod_get_index(xkbState.keymap, "Mod5")))
            xEvent.xkey.state |= Mod5Mask;
    }

    /*
     * Store the entire committed string once and send a single synthetic
     * KeyPress to deliver it.  TkpGetString reads and clears pendingText
     * on the first call, so per-character queuing loses all characters
     * after the first.  A single event carrying the full string avoids
     * that: the widget's <<Key>> binding calls TkpGetString once and gets
     * the complete UTF-8 string.
     *
     * Codepoint 0 is used as the routing token — it is in the synthetic
     * range and will never match a real key binding.
     */
    TkWaylandSetStoredText(topPtr, utf8_str);
    xEvent.xkey.keycode = SYNTHETIC_KEYCODE(0);
    Tk_QueueWindowEvent(&xEvent, TCL_QUEUE_TAIL);
}

/*
 * ----------------------------------------------------------------------------
 * OnCommitText --
 *
 *	    D-Bus signal handler for the IBus "CommitText" signal. Called when
 *	    the IME has finished composing a text string and it should be
 *	    inserted into the focused widget.
 *
 *	    Wire signature: CommitText(v) where v contains an IBus.Text struct.
 *
 * Results:
 *	    Returns 0 on success, or a negative error code on failure.
 *
 * Side effects:
 *	    Queues key events and virtual events for the target sub-widget.
 * ----------------------------------------------------------------------------
 */

static int
OnCommitText(
    sd_bus_message *m,
    void *userdata,
    sd_bus_error *ret_error)
{
    IbusContext *ctx = (IbusContext *)userdata;
    const char *text = NULL;
    int r;

    fprintf(stderr, ">>> OnCommitText\n");

    /*
     * Guard against signals that arrive after the Tk window has been
     * destroyed.  RemoveIbusContext sets this flag before freeing, but
     * in-flight D-Bus messages may still call back here.
     */
    if (!ctx || ctx->destroyed) {
        return 0;
    }

    r = IbusReadTextFromVariant(m, &text);
    if (r < 0 || !text) {
        return 0;
    }

    /*
     * Use the focused sub-widget recorded at FocusIn time.  Do NOT
     * dereference dispPtr->focusPtr here: on Wayland that pointer is not
     * reliably kept in sync and may be stale, causing "not found in
     * dictionary" errors and segfaults.
     */
    Tk_Window focusWin = ctx->focusedWidget ? ctx->focusedWidget : ctx->tkwin;

    /* Clear any marked/preedit region before inserting committed text. */
    Tk_SendVirtualEvent(focusWin, "TkClearIMEMarkedText", NULL);

    if (*text) {
        TkWaylandSendUnicodeString(focusWin, text);
    }

    /* Composition is complete — clear preedit state and composing flag. */
    if (ctx->preeditText) {
        free(ctx->preeditText);
        ctx->preeditText = NULL;
    }
    ctx->composing = 0;

    return 0;
}
/*
 * ----------------------------------------------------------------------------
 * OnUpdatePreedit --
 *
 *         D-Bus signal handler for the IBus "UpdatePreeditText" signal.
 *         Called when the IME preëdit string (the ongoing composition) changes.
 *
 *         Wire signature: UpdatePreeditText(v, u, b)
 *           v – IBus.Text variant (the preëdit string)
 *           u – uint32 cursor position within the preëdit
 *           b – bool visible
 *
 *         The preëdit is displayed using the standard Tk virtual events:
 *           <<TkStartIMEMarkedText>>, <<TkEndIMEMarkedText>>, and
 *           <<TkClearIMEMarkedText>>.  The preëdit text itself is inserted
 *         as synthetic key events (just like committed text) but between the
 *         start and end markers, causing the Tk bindings to underline or
 *         select the marked region.
 *
 * Results:
 *         Returns 0 on success, or a negative error code on failure.
 *
 * Side effects:
 *         Sends virtual events and synthetic key events.
 *         Sets ctx->composing = 1 while composition is active.
 * ----------------------------------------------------------------------------
 */

static int
OnUpdatePreedit(
    sd_bus_message *m,
    void *userdata,
    sd_bus_error *ret_error)
{
    IbusContext *ctx = (IbusContext *)userdata;
    const char *text = NULL;
    uint32_t cursor_pos = 0;
    int32_t visible = 0;
    int r;

    fprintf(stderr, ">>> OnUpdatePreedit\n");

    /* Guard against post-destruction callbacks (see OnCommitText). */
    if (!ctx || ctx->destroyed) {
        return 0;
    }

    r = IbusReadTextFromVariant(m, &text);
    if (r < 0) {
        return 0;
    }

    r = sd_bus_message_read(m, "ub", &cursor_pos, &visible);
    if (r < 0) {
        return 0;
    }

    /*
     * Use the widget recorded at FocusIn time, not dispPtr->focusPtr.
     * See OnCommitText for rationale.
     */
    Tk_Window focusWin = ctx->focusedWidget ? ctx->focusedWidget : ctx->tkwin;

    /* Always clear previous marked region first. */
    Tk_SendVirtualEvent(focusWin, "TkClearIMEMarkedText", NULL);

    if (!visible || !text || !*text) {
        if (ctx->preeditText) {
            free(ctx->preeditText);
            ctx->preeditText = NULL;
        }
        ctx->composing = 0;
        return 0;
    }

    /* Update stored preedit text and mark composition as active. */
    if (ctx->preeditText) {
        free(ctx->preeditText);
    }
    ctx->preeditText = strdup(text);
    ctx->composing = 1;

    /* Show new preedit. */
    Tk_SendVirtualEvent(focusWin, "TkStartIMEMarkedText", NULL);
    TkWaylandSendUnicodeString(focusWin, text);
    Tk_SendVirtualEvent(focusWin, "TkEndIMEMarkedText", NULL);

    return 0;
}

/*
 * ----------------------------------------------------------------------------
 * OnHidePreeditText --
 *
 *         D-Bus signal handler for the IBus "HidePreeditText" signal.
 *         Called when the IME wants the preëdit to be hidden (e.g., after
 *         committing or cancelling composition).
 *
 *         Wire signature: HidePreeditText()  — no arguments.
 *
 * Results:
 *         Returns 0.
 *
 * Side effects:
 *         Sends TkClearIMEMarkedText virtual event and clears composing state.
 * ----------------------------------------------------------------------------
 */

static int
OnHidePreeditText(
    sd_bus_message *m,
    void *userdata,
    sd_bus_error *ret_error)
{
    IbusContext *ctx = (IbusContext *)userdata;

    fprintf(stderr, ">>> OnHidePreeditText\n");

    if (!ctx || ctx->destroyed) {
        return 0;
    }

    Tk_Window focusWin = ctx->focusedWidget ? ctx->focusedWidget : ctx->tkwin;
    Tk_SendVirtualEvent(focusWin, "TkClearIMEMarkedText", NULL);

    if (ctx->preeditText) {
        free(ctx->preeditText);
        ctx->preeditText = NULL;
    }
    ctx->composing = 0;

    return 0;
}

